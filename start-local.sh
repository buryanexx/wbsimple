    echo "‚ö†Ô∏è –î–ª—è –ø–æ–ª–Ω–æ–π —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª—å–Ω–æ—Å—Ç–∏ —É—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –∏ –∑–∞–ø—É—Å—Ç–∏—Ç–µ PostgreSQL:"
    echo "  Mac: brew install postgresql && brew services start postgresql"
    echo "  Ubuntu: sudo apt install postgresql && sudo systemctl start postgresql"
    echo "  Windows: –°–∫–∞—á–∞–π—Ç–µ –∏ —É—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ —Å https://www.postgresql.org/download/windows/"
    
    # –ú–æ–¥–∏—Ñ–∏–∫–∞—Ü–∏—è .env –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è SQLite –≤–º–µ—Å—Ç–æ PostgreSQL, –µ—Å–ª–∏ —ç—Ç–æ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è –ø—Ä–æ–µ–∫—Ç–æ–º
