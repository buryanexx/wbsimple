    echo "–°–æ–∑–¥–∞–Ω–∏–µ –∏–∑ –ø—Ä–∏–º–µ—Ä–∞..."
    
    if [ -f ./client/.env.example ]; then
        cp ./client/.env.example ./client/.env
        echo "‚úÖ –°–æ–∑–¥–∞–Ω ./client/.env"
    else
        echo "‚ùå –û—à–∏–±–∫–∞: ./client/.env.example –Ω–µ –Ω–∞–π–¥–µ–Ω"
        exit 1
    fi
