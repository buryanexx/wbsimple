  echo "‚ùå –û—à–∏–±–∫–∞: npm –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω" >&2
  echo "–£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ npm —Å https://nodejs.org/"
  exit 1
